#import "DeviceViewController.h"

@interface DeviceViewController ()
@property (weak, nonatomic) IBOutlet UITextField *deviceName;
@property (weak, nonatomic) IBOutlet UITextField *deviceVersion;

@property (weak, nonatomic) IBOutlet UITextField *deviceCompany;
@end


@implementation DeviceViewController

- (IBAction)save:(id)sender{
    
     NSManagedObjectContext *context = [self managedObjectContext];
    NSManagedObject *device=[NSEntityDescription insertNewObjectForEntityForName:@"Device" inManagedObjectContext:context];
    [device setValue:self.deviceName.text forKey:@"name"];
     [device setValue:self.deviceVersion.text forKey:@"version"];
     [device setValue:self.deviceCompany.text forKey:@"companys"];
    NSError *error = nil;
    // Save the object to persistent store
    if (![context save:&error]) {
        NSLog(@"Can't Save! %@ %@", error, [error localizedDescription]);
    }
    
    [self dismissViewControllerAnimated:YES completion:nil];
    
}
- (NSManagedObjectContext *)managedObjectContext {
    NSManagedObjectContext *context = nil;
    id delegate = [[UIApplication sharedApplication] delegate];
    if ([delegate performSelector:@selector(managedObjectContext)]) {
        context = [delegate managedObjectContext];
    }
    return context;
}

- (IBAction)cancel:(id)sender{
    [self dismissViewControllerAnimated:YES completion:nil];


}

